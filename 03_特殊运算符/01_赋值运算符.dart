void main() {
  // 基本赋值运算符 =
  int a = 10;
  print('a = $a');

  // 复合赋值运算符
  a += 5; // 等价于 a = a + 5
  print('a += 5 -> $a');

  a -= 3; // 等价于 a = a - 3
  print('a -= 3 -> $a');

  a *= 2; // 等价于 a = a * 2
  print('a *= 2 -> $a');

  a ~/= 4; // 等价于 a = a ~/ 4 (取整除)
  print('a ~/= 4 -> $a');

  a %= 3; // 等价于 a = a % 3
  print('a %= 3 -> $a');

  // ??= 运算符：如果变量为 null，则赋值
  int? b;
  b ??= 20;
  print('b ??= 20 -> $b');
  b ??= 30;
  print('b ??= 30 -> $b'); // b 已有值，不会再赋值

  // 总结
  print('a 的最终值: $a');
  print('b 的最终值: $b');
}
